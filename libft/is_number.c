#include "libft.h"

bool	is_number(char *str)
{
	int	i;
	
	i = 0;
	while(str[i] != '\0')
	{
		if (str[i] >= '0' && str[i] <= '9')
			i++;
		else
			return (false);
	}
	return (true);

}