#include "libft.h"

int	ft_atoi(const char *str)
{
	int	n;
	int	t;

	n = 0;
	t = 1;
	while (*str == ' ' || *str == '\n' || *str == '\t' || *str == '\v'
		|| *str == '\f' || *str == '\r')
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			t = -1;
		str++;
	}
	while ((*str >= '0' && *str <= '9'))
	{
		n *= 10;
		n += *str - '0';
		str++;
	}
	return (n * t);
}
